/*
    :: Rest parameter ::
    - rest parameter is an improved way to handle function parameters, allowing us to more easily handle various input as parameters in a function. The rest parameter syntax allows us to represent an indefinite number of arguments as an array.
eg:

function sum(...inputs)
{
    console.log(...inputs) --> print like values
    console.log(inputs) --> Now its array
}

sum(1,2,3,4,5,6,7,8);


*/